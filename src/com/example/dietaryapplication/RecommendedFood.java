package com.example.dietaryapplication;

public class RecommendedFood {
	
	private FoodItem vegetables, fruit, milk, rice, meat, sugar, oil, egg;

	public RecommendedFood() {
		
	}

	public FoodItem getVegetables() {
		return vegetables;
	}

	public void setVegetables(FoodItem vegetables) {		
			this.vegetables = vegetables;			
	}

	public FoodItem getFruit() {
		return fruit;
	}

	public void setFruit(FoodItem fruit) {
		this.fruit = fruit;
	}

	public FoodItem getMilk() {
		return milk;
	}

	public void setMilk(FoodItem milk) {
		this.milk = milk;
	}

	public FoodItem getRice() {
		return rice;
	}

	public void setRice(FoodItem rice) {
		this.rice = rice;
	}

	public FoodItem getMeat() {
		return meat;
	}

	public void setMeat(FoodItem meat) {
		this.meat = meat;
	}

	public FoodItem getSugar() {
		return sugar;
	}

	public void setSugar(FoodItem sugar) {
		this.sugar = sugar;
	}

	public FoodItem getOil() {
		return oil;
	}

	public void setOil(FoodItem oil) {
		this.oil = oil;
	}

	public FoodItem getEgg() {
		return egg;
	}

	public void setEgg(FoodItem egg) {
		this.egg = egg;
	}
	
	
	
	

}
